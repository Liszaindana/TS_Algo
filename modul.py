
def penjumlahan(bil1 ,bil2):
    jumlah = bil1 + bil2
    print(f'hasil penjumlahan : {jumlah}')

def pengurangan(bil1 ,bil2):
    jumlah =  bil1 - bil2
    print(f'hasil pengerungan : {jumlah}')

def perkalian(bil1 ,bil2):
    jumlah = bil1 * bil2
    print(f'hasil perkalian : {jumlah}')